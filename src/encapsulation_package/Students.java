package encapsulation_package;

public class Students {
	
	private int rno;
	private String name;
	
	public void set_rno(int rno) {
		this.rno = rno;
	}
	public int get_rno() {
		return this.rno;
	}
	
	
	public void set_name(String name) {
		this.name = name;
	}
	public String get_name() {
		return this.name;
	}

}
